package model;

public class Sarkı {
    private int sarkıID;
    private String sarkıAdi;
    private int albumID;

    public int getSarkıID() {
        return sarkıID;
    }

    public void setSarkıID(int sarkıID) {
        this.sarkıID = sarkıID;
    }

    public String getSarkıAdi() {
        return sarkıAdi;
    }

    public void setSarkıAdi(String sarkıAdi) {
        this.sarkıAdi = sarkıAdi;
    }

    public int getAlbumID() {
        return albumID;
    }

    public void setAlbumID(int albumID) {
        this.albumID = albumID;
    }
}
